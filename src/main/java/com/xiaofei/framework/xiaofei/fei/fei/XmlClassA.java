public class XmlClassA {
    private Integer i;
    private String s;

    public Integer getI() {
        return i;
    }

    public void setI(Integer i) {
        this.i = i;
    }

    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }

    @Override
    public String toString() {
        return "XmlClassA{" +
                "i=" + i +
                ", s='" + s + '\'' +
                '}';
    }
}
